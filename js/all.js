$(document).ready(function () {
  // 打開aside選單按鈕
  $('.mobile-link').click(function (event) {
    event.stopPropagation();
    $('body').addClass('open');
    $(this).hide();
    $('.mainserch').hide();

  });
  // 關閉aside選單按鈕
  $('.mobile-close').click(function (event) {
    $('body').removeClass('open');
    $('.mobile-link').show();
    $('.mainserch').show();
  });

});

//點選aside外區域選單收合起來效果
//選擇 body 時可以關閉選單，而在打開的按鈕(mobile-link)以及選單本身上(aside)可以用 stopPropagation()
//讓事件不會往上傳遞，也就不會觸發到 body 所綁定的關閉選單事件

//hasClass() 方法檢查被選元素是否包含指定的 class

$('.aside').click(function(event){
  event.stopPropagation();
})

